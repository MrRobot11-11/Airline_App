  /*totalFair method returns destination price 
   *       + fare price(economy or first class) 
   *     + discount if under 12
   *Returns int*/
    public int totalFair(){
       int fare = 0;
       int destinationPrice = 0;
        if(flyer.isUnder12() == true){
          fare += fares.getUNDER_12_PRICE() +flyer.getFarePrice()+ flyer.getDestinationPrice();
       }
       if(flyer.isUnder12() == false){
           fare += flyer.getFarePrice() + flyer.getDestinationPrice();
       }
       return fare;
    }

