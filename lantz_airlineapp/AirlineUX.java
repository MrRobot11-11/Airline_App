       if(isInteger(txtBagWeight.getText()) == false) {
           JOptionPane.showMessageDialog(null, "Please enter a valid number");
       }
if(bagWeight > 50){
            JOptionPane.showMessageDialog(null, "Bag Weight must be 50 pounds or less.");
        }

